				for h = 1, #contingences do
					local contingence = contingences[h]
					if contingence then
						contingence:moveTo(Position(32737, 32117, 10))
					end
				end
			end
			Game.setStorageValue(Storage.Quest.U11_02.ForgottenKnowledge.ActiveTree, 1)
			addEvent(revertTree, 1 * 60 * 1000)
